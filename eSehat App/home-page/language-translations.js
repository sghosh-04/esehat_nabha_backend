// Language Management
class LanguageManager {
    constructor() {
        this.currentLang = this.getSavedLanguage() || 'en';
        this.init();
    }
    
    getSavedLanguage() {
        return localStorage.getItem('preferredLanguage');
    }
    
    saveLanguage(lang) {
        localStorage.setItem('preferredLanguage', lang);
    }
    
    init() {
        this.setupEventListeners();
        this.setLanguage(this.currentLang);
    }
    
    setLanguage(lang) {
        this.currentLang = lang;
        this.saveLanguage(lang);
        this.updatePageLanguage(lang);
        this.updateLanguageSelector(lang);
    }
    
    updatePageLanguage(lang) {
        console.log('Updating language to:', lang);
        
        // Update html lang attribute
        document.documentElement.lang = lang;
        
        // Update page title
        const titleKey = 'page_title';
        if (translations[lang] && translations[lang][titleKey]) {
            document.title = translations[lang][titleKey];
        }
        
        // Translate all elements with data-translate attribute
        const translatableElements = document.querySelectorAll('[data-translate]');
        console.log('Found translatable elements:', translatableElements.length);
        
        translatableElements.forEach(element => {
            const key = element.getAttribute('data-translate');
            if (translations[lang] && translations[lang][key]) {
                element.textContent = translations[lang][key];
            }
        });
        
        // Translate placeholder attributes
        const placeholderElements = document.querySelectorAll('[data-translate-placeholder]');
        placeholderElements.forEach(element => {
            const key = element.getAttribute('data-translate-placeholder');
            if (translations[lang] && translations[lang][key]) {
                element.placeholder = translations[lang][key];
            }
        });
        
        // Translate select options
        this.translateSelectOptions(lang);
    }
    
    translateSelectOptions(lang) {
        const selects = document.querySelectorAll('select');
        selects.forEach(select => {
            Array.from(select.options).forEach(option => {
                const key = option.getAttribute('data-translate');
                if (key && translations[lang] && translations[lang][key]) {
                    option.textContent = translations[lang][key];
                }
            });
        });
    }
    
    updateLanguageSelector(lang) {
        const languageSelect = document.getElementById('languageSelect');
        if (languageSelect) {
            languageSelect.value = lang;
        }
    }
    
    setupEventListeners() {
        const languageSelect = document.getElementById('languageSelect');
        if (languageSelect) {
            languageSelect.addEventListener('change', (e) => {
                console.log('Language changed to:', e.target.value);
                this.setLanguage(e.target.value);
            });
        } else {
            console.error('Language select element not found!');
        }
    }
}

// Initialize language manager when DOM is loaded
document.addEventListener('DOMContentLoaded', () => {
    console.log('DOM loaded, initializing language manager...');
    const languageManager = new LanguageManager();
    window.languageManager = languageManager;
});