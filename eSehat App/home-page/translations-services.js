// Language management functions
let currentLanguage = 'en';

function setLanguage(lang) {
    if (translations[lang]) {
        currentLanguage = lang;
        applyTranslations();
        // Save preference to localStorage
        localStorage.setItem('preferredLanguage', lang);
        
        // Update the language selector
        const languageSelect = document.getElementById('languageSelect');
        if (languageSelect) {
            languageSelect.value = lang;
        }
    }
}

function applyTranslations() {
    // Translate all elements with data-translate attribute
    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[currentLanguage] && translations[currentLanguage][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[currentLanguage][key];
            } else if (element.tagName === 'OPTION') {
                // Handle option elements separately
                element.textContent = translations[currentLanguage][key];
            } else {
                // For regular elements, preserve HTML structure
                const translation = translations[currentLanguage][key];
                if (element.innerHTML.includes('<strong>') || element.innerHTML.includes('<i>')) {
                    // Preserve HTML tags in translation
                    const tempDiv = document.createElement('div');
                    tempDiv.innerHTML = translation;
                    if (tempDiv.innerHTML === translation) {
                        // No HTML tags in translation, replace entire content
                        element.innerHTML = translation;
                    } else {
                        // Translation contains HTML, replace carefully
                        element.innerHTML = translation;
                    }
                } else {
                    element.textContent = translation;
                }
            }
        }
    });
    
    // Special handling for elements without data-translate attribute but need translation
    translateDynamicContent();
}

function translateDynamicContent() {
    // Translate dynamic content that might be added by services.js
    const freeBadges = document.querySelectorAll('.free-badge span');
    freeBadges.forEach(badge => {
        if (badge.textContent.includes('Free') || badge.textContent.includes('मुफ्त') || badge.textContent.includes('ਮੁਫ਼ਤ')) {
            badge.textContent = translations[currentLanguage]['free_services'];
        }
    });
    
    // Translate service cards that are dynamically loaded
    const serviceCards = document.querySelectorAll('.service-card');
    serviceCards.forEach(card => {
        const title = card.querySelector('h3');
        const description = card.querySelector('p');
        const button = card.querySelector('.service-btn');
        
        if (title && translations[currentLanguage][title.textContent]) {
            title.textContent = translations[currentLanguage][title.textContent];
        }
        
        if (button) {
            button.textContent = translations[currentLanguage]['learn_more'];
        }
    });
}

function getTranslation(key) {
    return translations[currentLanguage][key] || key;
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function() {
    // Check for saved language preference
    const savedLanguage = localStorage.getItem('preferredLanguage');
    if (savedLanguage && translations[savedLanguage]) {
        currentLanguage = savedLanguage;
    }
    
    // Set up language selector
    const languageSelect = document.getElementById('languageSelect');
    if (languageSelect) {
        languageSelect.value = currentLanguage;
        languageSelect.addEventListener('change', function() {
            setLanguage(this.value);
        });
    }
    
    // Apply translations immediately
    applyTranslations();
    
    // Re-apply translations after a short delay to catch dynamically loaded content
    setTimeout(applyTranslations, 100);
});

// Make functions available globally
window.translations = translations;
window.setLanguage = setLanguage;
window.getTranslation = getTranslation;
window.currentLanguage = currentLanguage;
window.applyTranslations = applyTranslations;

// Export for module use (if needed)
if (typeof module !== 'undefined' && module.exports) {
    module.exports = {
        translations,
        setLanguage,
        getTranslation,
        currentLanguage
    };
}