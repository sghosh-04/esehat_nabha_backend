// Current language
let currentLanguage = 'en';

// Initialize translation system
function initTranslations() {
    // Get saved language from localStorage or default to English
    const savedLanguage = localStorage.getItem('eSehatLanguage');
    if (savedLanguage && translations[savedLanguage]) {
        currentLanguage = savedLanguage;
        document.getElementById('languageSelect').value = currentLanguage;
    }
    
    // Apply translations
    applyTranslations();
    
    // Set up language selector change event
    document.getElementById('languageSelect').addEventListener('change', function(e) {
        currentLanguage = e.target.value;
        localStorage.setItem('eSehatLanguage', currentLanguage);
        applyTranslations();
    });
}

// Apply translations to the page
function applyTranslations() {
    // Get all elements with data-translate attribute
    const translatableElements = document.querySelectorAll('[data-translate]');
    
    translatableElements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[currentLanguage] && translations[currentLanguage][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[currentLanguage][key];
            } else {
                element.textContent = translations[currentLanguage][key];
            }
        }
    });
    
    // Update voice status text
    const voiceStatus = document.getElementById('voiceStatus');
    if (voiceStatus && currentLanguage === 'en') {
        voiceStatus.textContent = 'Click to start voice command';
    } else if (voiceStatus && currentLanguage === 'hi') {
        voiceStatus.textContent = 'वॉइस कमांड शुरू करने के लिए क्लिक करें';
    } else if (voiceStatus && currentLanguage === 'pa') {
        voiceStatus.textContent = 'ਵੌਇਸ ਕਮਾਂਡ ਸ਼ੁਰੂ ਕਰਨ ਲਈ ਕਲਿੱਕ ਕਰੋ';
    }
}

// Initialize when DOM is loaded
document.addEventListener('DOMContentLoaded', initTranslations);